    private void get_tbl_ProjectIssue(int Scheme_Id)
    {
        DataSet ds = new DataSet();
        ds = (new DataLayer()).get_tbl_ProjectIssue(Scheme_Id);
        if (ds != null && ds.Tables.Count > 0 && ds.Tables[0].Rows.Count > 0)
        {
            AllClasses.FillDropDown(ds.Tables[0], ddlIssueType, "ProjectIssue_Name", "ProjectIssue_Id");
        }
        else
        {
            ddlIssueType.Items.Clear();
        }
    }

    protected void rbtSearchBy_SelectedIndexChanged(object sender, EventArgs e)
