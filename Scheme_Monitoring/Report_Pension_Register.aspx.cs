        }
        if (txtYear.Text == "")
        {
            MessageBox.Show("Please Provide Year");
            txtYear.Focus();
            return;
        }
        get_Pension_Register_Summery();
    }


    protected void grdPost_RowDataBound(object sender, GridViewRowEventArgs e)
    {
        if (e.Row.RowType == DataControlRowType.Header)
        {
            e.Row.Cells[4].Text = Session["Default_Zone"].ToString();
            e.Row.Cells[5].Text = Session["Default_Circle"].ToString();
            e.Row.Cells[6].Text = Session["Default_Division"].ToString();
        }
    }

    protected void btnView_Click(object sender, ImageClickEventArgs e)
    {
        ImageButton btnView = sender as ImageButton;
        GridViewRow gr = btnView.Parent.Parent as GridViewRow;
        int Zone_Id = 0;
        int Circle_Id = 0;
        int Division_Id = 0;
        try
        {
            Zone_Id = Convert.ToInt32(gr.Cells[0].Text.Trim());
        }
        catch
        {
            Zone_Id = 0;
        }
        try
        {
            Circle_Id = Convert.ToInt32(gr.Cells[1].Text.Trim());
        }
        catch
        {
            Circle_Id = 0;
        }
        try
        {
            Division_Id = Convert.ToInt32(gr.Cells[2].Text.Trim());
        }
        catch
        {
            Division_Id = 0;
        }
        Response.Redirect("Report_Pension_Register_Details.aspx?Zone_Id=" + Zone_Id.ToString() + "&Circle_Id=" + Circle_Id.ToString() + "&Division_Id=" + Division_Id.ToString() + "&Month=" + ddlMonth.SelectedValue + "&Year=" + txtYear.Text.Trim());
    }

    protected void btnGenerate_Click(object sender, EventArgs e)
    {
        if (ddlMonth.SelectedValue == "0")
        {
            MessageBox.Show("Please Select A Month");
            ddlMonth.Focus();
            return;
        }
        if (txtYear.Text == "")
        {
            MessageBox.Show("Please Provide Year");
            txtYear.Focus();
            return;
        }
        if((new DataLayer()).Generate_Salary(Convert.ToInt32(ddlMonth.SelectedValue), Convert.ToInt32(txtYear.Text)))
        {
            get_Pension_Register_Summery();
            MessageBox.Show("Pension For Selected Month Generated Successfully");
            return;
        }
        else
        {
            MessageBox.Show("Error In Pension Generation");
            return;
        }
    }
