    private void get_TAT_Config()
    {
        int Project_Id = 0;
        
        try
        {
            Project_Id = Convert.ToInt32(ddlSearchScheme.SelectedValue);
        }
        catch
        {
            Project_Id = 0;
        }
        
        DataSet ds = new DataSet();
        ds = (new DataLayer()).get_TAT_Config(Project_Id);
        if (ds != null && ds.Tables.Count > 0 && ds.Tables[0].Rows.Count > 0)
        {
            grdTATConfig.DataSource = ds.Tables[0];
            grdTATConfig.DataBind();
        }
        else
        {
            grdTATConfig.DataSource = null;
            grdTATConfig.DataBind();
        }
    }

    private void get_TAT_Report()
    {
        int Project_Id = 0;
        int Zone_Id = 0;
        int Circle_Id = 0;
        int Division_Id = 0;

        try
        {
            Project_Id = Convert.ToInt32(ddlSearchScheme.SelectedValue);
        }
        catch
        {
            Project_Id = 0;
        }
        try
        {
            Zone_Id = Convert.ToInt32(ddlZone.SelectedValue);
        }
        catch
        {
            Zone_Id = 0;
        }
        try
        {
            Circle_Id = Convert.ToInt32(ddlCircle.SelectedValue);
        }
        catch
        {
            Circle_Id = 0;
        }
        try
        {
            Division_Id = Convert.ToInt32(ddlDivision.SelectedValue);
        }
        catch
        {
            Division_Id = 0;
        }
        DataSet ds = new DataSet();
        if (rbtSearchBy.SelectedValue == "1")
        {
            ds = (new DataLayer()).get_TAT_Report(Zone_Id, Circle_Id, Division_Id, Project_Id, "", "");
        }
        else
        {
            ds = (new DataLayer()).get_TAT_Report(Zone_Id, Circle_Id, Division_Id, Project_Id, txtDateFrom.Text, txtDateTill.Text);
        }

        if (ds != null && ds.Tables.Count > 0 && ds.Tables[0].Rows.Count > 0)
        {
            grdPost.AutoGenerateColumns = true;
            grdPost.DataSource = ds.Tables[0];
            grdPost.DataBind();

            List<string> _columnsList = new List<string>();

            for (int i = 0; i < ds.Tables[0].Columns.Count; i++)
            {
                _columnsList.Add(null);
            }
            _columnsList.Add(null);
            string[] columnsList = new string[_columnsList.Count];
            columnsList = _columnsList.ToArray();
            JavaScriptSerializer jss = new JavaScriptSerializer();
            hf_dt_Options_Dynamic.Value = jss.Serialize(columnsList);

            divData.Visible = true;
        }
        else
        {
            divData.Visible = false;
            grdPost.DataSource = null;
            grdPost.DataBind();
            MessageBox.Show("No Records Found");
        }
    }

